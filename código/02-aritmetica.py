'''
Aquí vemos y probamos los diferentes operadores
matemáticos dentro de python
'''

# SUMA
# algo entre comillas se llama string
print("Primera operación, suma:", 4 + 1)
# RESTA
print("Segunda operación, resta:", 300 - 275)
# MULTIPLICACIÓN
print("Tercera operación, multiplicación:", 3 * 5)
# DIVISIÓN
print("Cuarta operación, división:", 100 / 3)
# MÓDULO
print("Quinta operación, módulo:", 18 % 8)

'''
Aquí vemos y probamos los diferentes operadores
lógicos dentro de python
'''
# DESIGUALDADES
# < menor que
# > mayor que
# <= menor o igual que
# >= mayor o igual que
print("Es verdad que 3 < 5?" + " :)")
print(3 < 5)
print("Es verdad la siguiente expresión? 3+2 < 5-7")
print(3+2 < 5-7)
print("Es mayor que?", 3+2 > 5-7)
print("Es mayor o igual que?", 3+2 >= 5-7)
print("Es menor o igual que?", 3+2 <= 5-7)
